package Oprators;

public class UnaryOperators {
    
    public static void main(String[] args) {
        
        int a=5;

        System.out.println("Before Post Increment:"+a++);
        System.out.println("After Post Increment:"+a);
        System.out.println("Before Pre Increment:"+ ++a);
        System.out.println("After Pre Increment:"+a);
        System.out.println("Before Post Decrement:"+ a--);
        System.out.println("After Post Decrement:"+ a);
        System.out.println("Before Pre Decrement:"+ --a);
        System.out.println("After Pre Decrement:"+ a);
 
    }
}
